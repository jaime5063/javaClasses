package com.class24;

public class ThisKeyword {
/*
 * This keyword:
 * - access instance variables and instance methods
 * - is used to refer to the current class instance/object
 * - using this keyword we can refer/access current class instance methods
 * 
 * Why do we need 'this' keyword?
 * - when local variables are the same as instance variables
 * - 'this' keyword will be pointing out to your instance variables
 * 
 * 'this' can be used with variables
 * 	this.variable name = we are referring to the instance variables
 * - if we have local variables and instance variables with same name, 
 * 		to differentiate --> use 'this' keyword
 * 
 */

}
