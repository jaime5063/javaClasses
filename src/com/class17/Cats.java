package com.class17;

public class Cats {
	
	String color, eyeColor, pawColor, breed, name, eat;
	int weight;
	
	
	void eatingWeight() {
		System.out.println(name+" loves to eat and weights "+weight+" pounds");
	}
	
	void playful() {
		System.out.println(name+" is of "+breed+" breed and loves to play");
	}
	
	void hiding() {
		System.out.println(name+" is good at hiding because of her "+color+" color and "
				+ eyeColor+" eye color");
	}
	
	void running() {
		System.out.println("With her "+pawColor+" paws, "+name+" loves running");
	}
	
	

}
