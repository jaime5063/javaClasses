package com.Class26;

public class RecapPolymorphism {
/*
 * Polymorphism
 * - Methods that can perform different actions
 * 		- Compile time / Static binding / Early Binding --> method Overloading
 * 	Method overloading = feature of when inside same class we have Methods with same name BUT
 * 						 different type and number of parameters.
 * 
 * 	- public void hello(){
 * 		SOPln("Hello");
 * }
 * public void hello(String str){
 * 		SOPln("Hello "+str);
 * }
 * private void hello(String num){  --> NOT overloading (Changed return type)
 * 		SOPln("Hello "+num);
 * } 
 * public String void hello(String str){  --> NOT overloading (Changed return type)
 * 		SOPln("Hello "+str);
 * 
 * 	Key points:
 * 	- we can overload static methods
 *  - we can overload private methods
 *  
 *  - Constructors can be overloaded
 *  
 *  Operator overloading (+)
 *  - With String --> behaves as concatenation operator
 *  - With numbers --> behaves as math operator
 *  
 * 
 * 	
 */

}
